package com.revature.models;


import com.amazonaws.services.dynamodbv2.datamodeling.*;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;


import java.time.LocalDateTime;
import java.util.List;

@Data
@NoArgsConstructor
@DynamoDBTable(tableName = "ForumNodes")
public class Node {

    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    private String id;

    @DynamoDBAttribute
    private String subject;

    @DynamoDBAttribute
    private List<String> ancestors;

    @DynamoDBIndexHashKey(attributeName = "parent", globalSecondaryIndexName = "parent-date_created-index")
    private String parent;

    @DynamoDBAttribute
    private String description;

    @DynamoDBAttribute
    private int childCount;


    @DynamoDBIndexRangeKey(attributeName = "date_created", globalSecondaryIndexName = "parent-date_created-index")
    private String dateCreated;

    @DynamoDBAttribute
    private String owner;

    @DynamoDBAttribute
    private List<String> tags;

    public Node(String id, String subject, List<String> ancestors, String parent, String description, int childCount, String dateCreated, String owner, List<String> tags) {
        this.id = id;
        this.subject = subject;
        this.description = description;
        this.ancestors = ancestors;
        this.parent = parent;
        this.childCount = childCount;
        this.dateCreated = dateCreated;
        this.owner = owner;
        this.tags = tags;

    }

}
